#!/usr/bin/env python3
"""
Discrete logarithm on a small prime — demonstrates that DLP difficulty depends
heavily on group size and structure.

Parameters (from reference-2.md):
  p = 2^32 - 5 = 4294967291
  p - 1 = 2 * 3 * 5 * 17 * 257 * 65537 * 6700417  (smooth)

Two attacks are demonstrated:
  1. Baby-step giant-step:   O(sqrt(p)) time, O(sqrt(p)) space.
  2. Pohlig–Hellman:         reduces the problem to small prime-order subgroups.

On a 32-bit prime both methods finish in well under a second; on a 2048-bit
prime with a large (p-1) factor they are infeasible.
"""

import math
import random
import time
from typing import List, Tuple


P = 2 ** 32 - 5
G = 3

# Full factorization of p - 1.
P_FACTORS_FULL = [(2, 1), (5, 1), (19, 1), (22605091, 1)]


def subgroup_order(g: int, p: int, factors: List[Tuple[int, int]]) -> Tuple[int, List[Tuple[int, int]]]:
    """Return the order of g and the factors of that order.

    ``factors`` must be the prime factorization of p - 1.  A prime factor q
    divides the order of g iff g^((p-1)/q) != 1 (mod p).
    """
    n = p - 1
    order = 1
    order_factors = []
    for prime, exp in factors:
        if pow(g, n // prime, p) != 1:
            # prime divides the order; include it with the same exponent.
            order *= prime ** exp
            order_factors.append((prime, exp))
    return order, order_factors


P_ORDER, P_FACTORS = subgroup_order(G, P, P_FACTORS_FULL)


# ---------------------------------------------------------------------------
# BSGS and Pohlig-Hellman
# ---------------------------------------------------------------------------


def mod_pow(base: int, exp: int, mod: int) -> int:
    return pow(base, exp, mod)


def mod_inv(a: int, mod: int) -> int:
    return pow(a, -1, mod)


def bsgs(g: int, h: int, p: int) -> int:
    """Baby-step giant-step: find x such that g^x = h (mod p)."""
    m = math.isqrt(p) + 1
    baby = {}
    cur = 1
    for j in range(m):
        if cur not in baby:
            baby[cur] = j
        cur = (cur * g) % p

    factor = mod_inv(mod_pow(g, m, p), p)
    gamma = h
    for i in range(m + 1):
        if gamma in baby:
            return i * m + baby[gamma]
        gamma = (gamma * factor) % p

    raise ValueError("h is not in the subgroup generated by g")


def crt(remainders: List[int], moduli: List[int]) -> int:
    """Chinese Remainder Theorem for pairwise-coprime moduli."""
    total = 0
    M = 1
    for m in moduli:
        M *= m
    for r, m in zip(remainders, moduli):
        M_i = M // m
        inv = mod_inv(M_i, m)
        total = (total + r * M_i * inv) % M
    return total


def pohlig_hellman(g: int, h: int, p: int, factors: List[Tuple[int, int]]) -> int:
    """
    Pohlig–Hellman decomposition.
    factors is the prime factorization of the order of g.
    """
    n = 1
    for prime, exp in factors:
        n *= prime ** exp

    residues = []
    moduli = []

    for prime, exp in factors:
        pe = prime ** exp

        # Reduce to subgroup of order pe.
        g_i = mod_pow(g, n // pe, p)
        h_i = mod_pow(h, n // pe, p)

        # Solve DLP in subgroup of order pe.
        x_i = bsgs(g_i, h_i, p) % pe

        residues.append(x_i)
        moduli.append(pe)

    return crt(residues, moduli)


def main():
    n_runs = 3
    seeds = [0x12345, 0xDEADBEEF, 0xC0FFEE]

    print(f"p       = {P} (2^32 - 5)")
    print(f"p-1     = {P - 1} = {' * '.join(f'{p}^{e}' if e > 1 else str(p) for p, e in P_FACTORS_FULL)}")
    print(f"order(g)= {P_ORDER} = {' * '.join(f'{p}^{e}' if e > 1 else str(p) for p, e in P_FACTORS)}")
    print(f"g       = {G}")
    print()

    bsgs_times = []
    ph_times = []
    all_recovered = True
    all_agree = True

    for run, seed in enumerate(seeds, start=1):
        random.seed(seed)
        x_secret = random.randint(1, P_ORDER - 1)
        h = mod_pow(G, x_secret, P)

        print(f"Run {run}: secret = {x_secret}, h = {h}")

        t0 = time.perf_counter()
        x_bsgs = bsgs(G, h, P)
        t1 = time.perf_counter()
        bsgs_times.append((t1 - t0) * 1000)
        bsgs_ok = mod_pow(G, x_bsgs, P) == h

        t0 = time.perf_counter()
        x_ph = pohlig_hellman(G, h, P, P_FACTORS)
        t1 = time.perf_counter()
        ph_times.append((t1 - t0) * 1000)
        ph_ok = mod_pow(G, x_ph, P) == h

        agree = (x_bsgs % P_ORDER) == (x_ph % P_ORDER)
        all_recovered = all_recovered and bsgs_ok and ph_ok
        all_agree = all_agree and agree

        print(f"  BSGS: x = {x_bsgs}, verify = {bsgs_ok}, time = {bsgs_times[-1]:.2f} ms")
        print(f"  PH:   x = {x_ph}, verify = {ph_ok}, time = {ph_times[-1]:.2f} ms")
        print(f"  BSGS/PH agree: {'YES' if agree else 'NO'}")
        print()

    avg_bsgs = sum(bsgs_times) / len(bsgs_times)
    avg_ph = sum(ph_times) / len(ph_times)
    print("Summary:")
    print(f"  Runs: {n_runs}")
    print(f"  BSGS time (min/avg/max): {min(bsgs_times):.2f} / {avg_bsgs:.2f} / {max(bsgs_times):.2f} ms")
    print(f"  PH time   (min/avg/max): {min(ph_times):.2f} / {avg_ph:.2f} / {max(ph_times):.2f} ms")
    print(f"  All secrets recovered correctly: {'YES' if all_recovered else 'NO'}")
    print(f"  BSGS and PH agree in all runs: {'YES' if all_agree else 'NO'}")


if __name__ == '__main__':
    main()
